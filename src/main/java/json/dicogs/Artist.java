package json.dicogs;

public class Artist {

	private String name;
	private String releases_url;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getReleases_url() {
		return releases_url;
	}

	public void setReleases_url(String releases_url) {
		this.releases_url = releases_url;
	}

}
